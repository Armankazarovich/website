export async function POST() {
  const session = await auth();
  if (!session?.user) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

  const existingPostsCount = await prisma.post.count();
  const existingServicesCount = await prisma.service.count();

  let postsCreated = 0;
  let servicesCreated = 0;

  if (existingPostsCount === 0) {
    for (const post of INITIAL_POSTS) {
      await prisma.post.create({ data: post });
      postsCreated++;
    }
  }

  if (existingServicesCount === 0) {
    for (const service of INITIAL_SERVICES) {
      await prisma.service.create({ data: service });
      servicesCreated++;
    }
  }

  return NextResponse.json({
    ok: true,
    postsCreated,
    servicesCreated,
    message:
      existingPostsCount > 0 && existingServicesCount > 0
        ? "Данные уже существуют, ничего не добавлено"
        : `Создано: ${postsCreated} статей, ${servicesCreated} услуг`,
  });
}
